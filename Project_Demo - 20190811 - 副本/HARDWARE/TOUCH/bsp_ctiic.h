#ifndef __BSP_CTIIC_H
#define __BSP_CTIIC_H

#include <bsp.h>

// IO方向设置
#define CT_SDA_IN()  {GPIOC->MODER&=~(3<<(2*3));GPIOC->MODER|=0<<2*3;}	// PC3输入模式
#define CT_SDA_OUT() {GPIOC->MODER&=~(3<<(2*3));GPIOC->MODER|=1<<2*3;} 	// PC3输出模式
// IO操作函数
#define CT_IIC_SCL    PCout(4) 	// SCL
#define CT_IIC_SDA    PCout(3)	// SDA	 
#define CT_READ_SDA   PCin(3)   // 输入SDA 


// IIC所有操作函数
void CT_IIC_Init(void);                		// 初始化IIC的IO口
void CT_IIC_Start(void);					// 发送IIC开始信号
void CT_IIC_Stop(void);	  					// 发送IIC停止信号
void CT_IIC_Send_Byte(uint8_t txd);			// IIC发送一个字节
uint8_t CT_IIC_Read_Byte(unsigned char ack);// IIC读取一个字节
uint8_t CT_IIC_Wait_Ack(void); 				// IIC等待ACK信号
void CT_IIC_Ack(void);						// IIC发送ACK信号
void CT_IIC_NAck(void);						// IIC不发送ACK信号

#endif /* __BSP_CTIIC_H */
